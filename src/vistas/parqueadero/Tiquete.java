    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtPlaca.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el número de placa.\nIntente nuevamente");
        } else {
            String buscado = txtPlaca.getText();
            boolean aux = controlador.registrarSalida(buscado);
            if(aux){
                 JOptionPane.showMessageDialog(null, "La salidad se ha registrado con éxito");
                 ventana.limpiar();
                 this.dispose();
            }else{
                 JOptionPane.showMessageDialog(null, "No se ha registrado con éxito");
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

