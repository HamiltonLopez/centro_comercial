 public void llenar() {
        Date fecha = new Date();
        txtHoraEntrada.setText(String.valueOf(vehiculo.getHoraEntrada()));
        txtHoraSalida.setText(String.valueOf(fecha));
        txtPlaca.setText(String.valueOf(vehiculo.getPlaca()));
        txtTipo.setText(String.valueOf(vehiculo.getTipo()));
        Vehiculo vehi =  controlador.calcularTiquete(vehiculo);
        txtHorasTotales.setText(String.valueOf(vehi.getHorasTotales()));
        txtTotal.setText(String.valueOf(vehi.getTotal()));
    }
