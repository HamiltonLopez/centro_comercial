     public void llenarTablaConcursos() {
        removerTablaConcurso();
        Lista<modelos.Concurso> concursos = controladorC.getConcursos();
        for (int i = 0; i < concursos.Size(); i++) {
            modelos.Concurso concurso = concursos.obtener(i);
            if (concurso != null) {
                String[] info = new String[6];
                info[0] = String.valueOf(concurso.getNumeroConcurso());
                info[1] = String.valueOf(concurso.getFechaInicio());
                info[2] = String.valueOf(concurso.getFechaFinal());
                info[3] = String.valueOf(concurso.getParticipantes().Size());
                info[4] = String.valueOf(concurso.getDetalleConcurso());
                info[5] = String.valueOf(concurso.getValorMinimo());

                modell.addRow(info);

            } else {

            }

        }
    }
     public void removerTablaConcurso() {
        for (int i = 0; i < modell.getRowCount(); i++) {
            modell.removeRow(i);
        }

    }

    public void limpiar() {

        removerTablaConcurso();

        llenarTablaConcursos();
    }

