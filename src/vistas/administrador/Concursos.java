    private void btnAleatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAleatorioActionPerformed
        if (tablaConcursos.getSelectionModel().isSelectionEmpty()) {
            JOptionPane.showMessageDialog(null, "Seleccione el concurso para elegir ganador");
        }  else {
            int fila = tablaConcursos.getSelectedRow();
            String identificador = tablaConcursos.getValueAt(fila, 0).toString();
            int numero = Integer.parseInt(identificador);
            Concurso concurso = controladorC.buscarConcurso(numero);
            Cliente cliente = controladorC.elegirCliente(concurso);
            if(cliente!= null){
                JOptionPane.showMessageDialog(null, "Felicidades " + cliente.getNombre()+ " has sido el ganador \nde un increible " +concurso.getDetalleConcurso());
            }else{
                JOptionPane.showMessageDialog(null, "El concurso aún no cuenta con participantes.");
            }
            
        }      
    }//GEN-LAST:event_btnAleatorioActionPerformed


