    public void llenarTablaClientes() {
        removerTabla();
        Lista<Usuario> usuarios = controladorU.getUsuarios();
        for (int i = 0; i < usuarios.Size(); i++) {
            Usuario cliente = usuarios.obtener(i);
            if (cliente != null && cliente instanceof Cliente) {
                String[] info = new String[4];
                info[0] = cliente.getNombre();
                info[1] = cliente.getDocumento();
                info[2] = cliente.getCorreo();
                info[3] = cliente.getTelefono();

                modelo.addRow(info);

            } else {

            }

        }
    }

    public void removerTabla() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
        }

    }

