    private void btnRegistrar6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrar6ActionPerformed
        if (txtValorMinimo.getText().isEmpty()
                || txtDescripcion.getText().isEmpty()||chooser.getDate()==null ||chooserFinal.getDate()==null) {
            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios");

        } else {
            Date inicio = chooser.getDate();
            Date fin = chooserFinal.getDate();
            int valor = Integer.parseInt(txtValorMinimo.getText());
            String descripcion = txtDescripcion.getText();
            Date fechaInicio = new Date(inicio.getYear(), inicio.getMonth(), inicio.getDay());
            Date fechaFinal = new Date(fin.getYear(), fin.getMonth(), fin.getDay());

            Concurso concurso = new Concurso(fechaInicio, fechaFinal, valor, descripcion);
            boolean aux = controlador.crearConcurso(concurso);
            if (aux) {
                JOptionPane.showMessageDialog(null, "El concurso se ha creado");
                Concurso concu = controlador.buscarConcurso(concurso.getNumeroConcurso());
                pilaZ.push(concu);
                limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "No se ha podido completar la acciÃ³n.");
            }
        }
    }//GEN-LAST:event_btnRegistrar6ActionPerformed

