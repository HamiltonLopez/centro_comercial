    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txtBuscar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el documento");
        } else {
            String documento = txtBuscar.getText();
            try {
                Cliente cliente = controlador.buscarCliente(documento);
                if (cliente == null) {
                   
                } else {
                    remover();
                    int total = controlador.buscarTotal(cliente.getDocumento());
                  
                    String[] info = new String[5];
                    info[0] = String.valueOf(cliente.getDocumento());
                    info[1] = String.valueOf(cliente.getNombre());
                    info[2] = String.valueOf(total);
                    model.addRow(info);
                }

            } catch (UsuarioNoEncontradoException ex) {
                ex.getMessage();
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

     public void remover() {

        for (int i = 0; i < model.getRowCount(); i++) {
            model.removeRow(i);
        }
    }
