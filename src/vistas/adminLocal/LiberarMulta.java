    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtBuscar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el documento");
        } else {
            String documento = txtBuscar.getText();
            try {
                Cliente cliente = controlador.getControlador().buscarCliente(documento);
                if (cliente.getMulta() == null) {
                    JOptionPane.showMessageDialog(null, "El cliente ingresado no posee multas activas");
                } else {
                    remover();
                    txtBuscar.setEditable(false);
                    String[] info = new String[5];
                    info[0] = String.valueOf(cliente.getDocumento());
                    info[1] = String.valueOf(cliente.getNombre());
                    info[2] = String.valueOf(cliente.getMulta().getDetalles());
                    model.addRow(info);
                }

            } catch (UsuarioNoEncontradoException ex) {
                ex.getMessage();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed
    public void remover() {

        for (int i = 0; i < model.getRowCount(); i++) {
            model.removeRow(i);
        }
    }
    private void btnLiberarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLiberarActionPerformed
        if (txtBuscar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el documento");
        } else {
            String documento = txtBuscar.getText();
            boolean aux = controlador.eliminarMulta(documento);
            if (aux) {
                JOptionPane.showMessageDialog(null, "La multa se ha liberado con éxito");
                txtBuscar.setEditable(true);
                remover();
            } else {
                JOptionPane.showMessageDialog(null, "La multa no se ha podido eliminar");
            }
        }
    }//GEN-LAST:event_btnLiberarActionPerformed

