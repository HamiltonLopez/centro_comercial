    private void btnFinalizarSeparadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarSeparadoActionPerformed
        if (tablaSeparados.getSelectionModel().isSelectionEmpty()) {
            JOptionPane.showMessageDialog(null, "Seleccione el separado que desea finalizar");
        } else {
            int fila = tablaSeparados.getSelectedRow();
            String documento = tablaSeparados.getValueAt(fila, 2).toString();
            Separado separado = controlador.buscarSeparado(documento);
            boolean aux = controlador.FinalizarSeparado(separado);
            if (aux) {
                JOptionPane.showMessageDialog(null, "Se ha finalizado con Ã©xito el separado");
                limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "No se ha podido finalizar");
            }

        }

    }//GEN-LAST:event_btnFinalizarSeparadoActionPerformed

