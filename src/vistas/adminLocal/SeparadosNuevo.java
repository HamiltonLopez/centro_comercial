    public void llenarTabla() {
        remover();
        Lista<Separado> separados = controlador.getSeparados();
        for (int i = 0; i < separados.Size(); i++) {
            Separado venta = separados.obtener(i);
            if (venta != null) {
                Producto producto = venta.getProducto();
                String[] info = new String[5];
                info[0] = String.valueOf(producto.getIdentificador());
                info[1] = String.valueOf(producto.getNombreProducto());

                info[2] = String.valueOf(venta.getCliente().getDocumento());
                info[3] = String.valueOf(venta.getFechaEntrega());

                model.addRow(info);

            } else {

            }

        }
    }

    public void limpiar() {
        remover();
        llenarTabla();
    }

    public void remover() {

        for (int i = 0; i < model.getRowCount(); i++) {
            model.removeRow(i);
        }
    }

