    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
        if (tablaSeparados.getSelectionModel().isSelectionEmpty()) {
            JOptionPane.showMessageDialog(null, "Seleccione el separado al que desea generar multa");
        } else if (txtArea.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el detalle del porquÃ© de la multa");
        } else {
            int fila = tablaSeparados.getSelectedRow();
            String documento = tablaSeparados.getValueAt(fila, 2).toString();
            String detalle = txtArea.getText();
            Separado separado = controlador.buscarSeparado(documento);
            Cliente cliente = controlador.buscarCliente(documento);
            Multa multa = new Multa(detalle, cliente);
            boolean aux = controlador.asignarMulta(multa, separado);
            if (aux) {
                JOptionPane.showMessageDialog(null, "Se ha asignado con Ã©xito la multa");
                limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "No se ha podido finalizar");
            }

        }
    }//GEN-LAST:event_btnAsignarActionPerformed


